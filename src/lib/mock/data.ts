/** 모든 계약서 조회 */
export function getContracts(): Contract[] {
  return mockContracts
}

/** 특정 계약서 ID로 조회 */
export function getContractById(id: string): Contract | undefined {
  return mockContracts.find((c) => c.id === id)
}

/** 상태별 계약서 수 카운트 */
export function getStatusCounts() {
  const contracts = mockContracts
  return {
    total: contracts.length,
    processing: contracts.filter(
      (c) => c.status === "ocr_processing" || c.status === "classifying"
    ).length,
    completed: contracts.filter((c) => c.status === "completed").length,
    review_required: contracts.filter((c) => c.status === "review_required")
      .length,
    failed: contracts.filter((c) => c.status === "failed").length,
  }
}
